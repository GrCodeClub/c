#include <stdio.h>

int main() {
    int numbers[5] = {1, 2, 3, 4, 5}; // Δημιουργούμε έναν πίνακα numbers με μέγεθος 5 και αρχικές τιμές
    int i; // Ορίζουμε μια μεταβλητή i για την επανάληψη του βρόχου

    printf("Ο πίνακας numbers: ");
    for (i = 0; i < 5; i++) { // Επαναλαμβάνουμε τον κώδικα για κάθε στοιχείο του πίνακα
        printf("%d ", numbers[i]); // Εκτυπώνουμε το τρέχον στοιχείο του πίνακα
    }

    return 0;
}
