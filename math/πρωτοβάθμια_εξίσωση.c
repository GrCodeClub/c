/* Να γραφεί συνάρτηση η οποία θα λύνει την πρωτοβάθμια εξίσωση. 
Η συνάρτηση θα επιστρέφει (στο όνομά της) 0 αν έχει λύση, 1 
αν είναι αδύνατη και δύο αν είναι αόριστη. Σε περίπτωση που έχει λύση θα επιστρέφει και τη ρίζα. */

#include <stdio.h>

int solveLinearEquation(double a, double b, double *solution, int *type) {
    if (a == 0) {
        if (b == 0) {
            // Αν ο συντελεστής a και b είναι και οι δύο μηδέν, η εξίσωση είναι αόριστη (0x = 0)
            *type = 2;
        } else {
            // Αν ο συντελεστής a είναι μηδέν, αλλά ο συντελεστής b διάφορος του μηδενός, η εξίσωση είναι αδύνατη (0x = c, όπου c ≠ 0)
            *type = 1;
        }
        return 1; // Επιστρέφει 1 για να υποδηλώσει ότι η εξίσωση δεν έχει λύση
    } else {
        // Υπολογισμός της ρίζας (x = -b/a)
        *solution = -b / a;
        *type = 0; // Ορίζει τον τύπο της λύσης ως 0 (έχει λύση)
        return 0; // Επιστρέφει 0 για να υποδηλώσει ότι η εξίσωση έχει λύση
    }
}

int main() {
    double a, b, solution;
    int type;

    printf("Δώστε τον συντελεστή a: ");
    scanf("%lf", &a);
    printf("Δώστε τον συντελεστή b: ");
    scanf("%lf", &b);

    int result = solveLinearEquation(a, b, &solution, &type);

    if (result == 0) {
        printf("Λύση: x = %lf\n", solution);
    } else if (result == 1) {
        if (type == 1) {
            printf("Η εξίσωση είναι αδύνατη.\n");
        } else if (type == 2) {
            printf("Η εξίσωση είναι αόριστη.\n");
        }
    }

    return 0;
}


